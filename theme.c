#include "theme.h"
#include <string.h>
#include <stdio.h>

#define THEME_COUNT 11

static Theme themes[THEME_COUNT];
static int currentThemeIndex = 0;


const Theme* getCurrentTheme() {
    return &themes[currentThemeIndex];
}

const char* getCurrentThemeName() {
    return themes[currentThemeIndex].name;
}

int getCurrentThemeIndex() {
    return currentThemeIndex;
}

int getThemeCount() {
    return THEME_COUNT;
}

void switchToTheme(int index) {
    if (index >= 0 && index < THEME_COUNT) {
        currentThemeIndex = index;
        printf("Switched to theme: %s\n", themes[currentThemeIndex].name);
    }
}

void nextTheme() {
    int nextIndex = (currentThemeIndex + 1) % THEME_COUNT;
    switchToTheme(nextIndex);
}

void previousTheme() {
    int prevIndex = (currentThemeIndex - 1 + THEME_COUNT) % THEME_COUNT;
    switchToTheme(prevIndex);
}

void loadThemeByName(const char *themeName) {
    for (int i = 0; i < THEME_COUNT; i++) {
        if (strcmp(themes[i].name, themeName) == 0) {
            switchToTheme(i);
            return;
        }
    }
    printf("Theme '%s' not found.\n", themeName);
}



#define SRGB_TO_LINEAR_FAST(c) \
    (((c) <= 0.04045f) ? ((c) / 12.92f) : ((c) * (c) * (0.9478672986f + (c) * 0.0521327014f)))

#define HEX_TO_COLOR_LINEAR(r, g, b) \
    (Color){ \
        SRGB_TO_LINEAR_FAST((r) / 255.0f), \
        SRGB_TO_LINEAR_FAST((g) / 255.0f), \
        SRGB_TO_LINEAR_FAST((b) / 255.0f), \
        1.0f \
    }




